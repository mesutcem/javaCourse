package loopDemo;

public class Main {

	public static void main(String[] args) {
			//For
		for(int i=1;i<=10;i++) {
			System.out.println(i);
		}
		System.out.println("end for the for");
				
		int i =1;
			//While
		while(i<10) {
			System.out.println(i);
			i++;			
		}
		System.out.println("end for the while");
		
			//Do-While
		int j = 1;
		do {
			System.out.println(j);
			j++;
		}
		while(j<10);
		System.out.println("end for the do-while");
	}

}
